#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	len;

	len = ft_strlen(s);
	if (s)
	{
		write(1, s, len);
	}
	else
	{
		write(1, "(null)", 6);
		return (6);
	}
	return (len);
}
